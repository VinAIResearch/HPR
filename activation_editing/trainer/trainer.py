class GuidanceTrainer(Trainer):

    def compute_loss(self, model, inputs, return_outputs=False):
        """
        The negative activations are forwarded through the model. The model is optimized to predict the distance from
        negative activations to positive activations.

        In order to ensure that the guidance modules do not guide generation away from the already positive direction,
        we also add a regularization term that penalizes the guidance modules if their outputs corresponding to
        positive activations are large.
        """
        outputs = ()
        loss = torch.tensor(0.0).to(self.args.device)
        lambda_p = getattr(self.args, "lambda_p", None)
        guidance_loss_fnc = GUIDANCE_LOSS_FUNCTIONS[model.guidance_config.guidance_module_type]

        for layer_idx in model.guidance_config.target_layers:
            negative = inputs[f"negative.{layer_idx}"]
            positive = inputs[f"positive.{layer_idx}"]
            guidance_module = model.guidance_modules[str(layer_idx)]
            loss_fnc_outputs = guidance_loss_fnc(guidance_module=guidance_module,
                                                 positive_activation=positive,
                                                 negative_activation=negative,
                                                 return_outputs=return_outputs,
                                                 lambda_p=lambda_p)

            if return_outputs:
                loss_, output = loss_fnc_outputs
                outputs += (output,)
            else:
                loss_ = loss_fnc_outputs
            loss += loss_

        # Scale loss down by the number of guidance modules.
        loss /= len(model.guidance_config.target_layers)
        return (loss, outputs) if return_outputs else loss

    def eval_loss(self, model, inputs, return_outputs=False):
        """
        This method is deprecated.

        The negative activations are forwarded through the model. The model is optimized to predict the distance from
        negative activations to positive activations.

        When doing evaluation, we drop the regularization term and only focus on the loss between the predicted distance
        and the true distance.
        """
        outputs = ()
        loss = torch.tensor(0.0).to(self.args.device)
        lambda_p = getattr(self.args, "lambda_p", 0)
        guidance_loss_fnc = GUIDANCE_LOSS_FUNCTIONS[model.guidance_config.guidance_module_type]

        for layer_idx in model.guidance_config.target_layers:
            negative = inputs[f"negative.{layer_idx}"]
            positive = inputs[f"positive.{layer_idx}"]
            guidance_module = model.guidance_modules[str(layer_idx)]
            loss_fnc_outputs = guidance_loss_fnc(guidance_module=guidance_module,
                                                 positive_activation=positive,
                                                 negative_activation=negative,
                                                 return_outputs=return_outputs,
                                                 lambda_p=lambda_p)

            if return_outputs:
                loss_, output = loss_fnc_outputs
                outputs += (output,)
            else:
                loss_ = loss_fnc_outputs
            loss += loss_

        # Scale loss down by the number of guidance modules.
        loss /= len(model.guidance_config.target_layers)
        return (loss, outputs) if return_outputs else loss

    def prediction_step(
        self,
        model: nn.Module,
        inputs: Dict[str, Union[torch.Tensor, Any]],
        prediction_loss_only: bool,
        ignore_keys: Optional[List[str]] = None,
    ) -> Tuple[Optional[torch.Tensor], Optional[torch.Tensor], Optional[torch.Tensor]]:
        """
        Perform an evaluation step on `model` using `inputs`.

        Only returns the MSE loss between the guidance modules' predictions and the difference between the positive
        activations and the negative activations.

        Args:
            model (`nn.Module`):
                The model to evaluate. Should already have guidance modules initialized.
            inputs (`Dict[str, Union[torch.Tensor, Any]]`):
                The inputs of the model. Contains a "positive.k" and a "negative.k" key for each layer index k in the
                base model.

                The dictionary will be unpacked before being fed to the model.
            prediction_loss_only (`bool`):
                Whether or not to return the loss only. However, this method returns only the prediction loss anyway.
            ignore_keys (`List[str]`, *optional*):
                A list of keys in the output of your model (if it is a dictionary) that should be ignored when
                gathering predictions.

        Return:
            Tuple[Optional[torch.Tensor], Optional[torch.Tensor], Optional[torch.Tensor]]: A tuple with the loss,
            logits and labels (each being optional). However, logits and labels will be omitted in this case.
        """
        # TODO: This method is messy and might be updated in the future.
        inputs = self._prepare_inputs(inputs)
        if ignore_keys is None:
            if hasattr(self.model, "config"):
                ignore_keys = getattr(self.model.config, "keys_to_ignore_at_inference", [])
            else:
                ignore_keys = []

        with torch.no_grad():
            if prediction_loss_only:
                with self.compute_loss_context_manager():
                    loss = self.compute_loss(model=model, inputs=inputs, return_outputs=False)
                return loss, None, None
            else:
                with self.compute_loss_context_manager():
                    loss, outputs = self.compute_loss(model=model, inputs=inputs, return_outputs=True)
                if isinstance(outputs, dict):
                    logits = tuple(v for k, v in outputs.items() if k not in ignore_keys)
                else:
                    logits = outputs

                logits = nested_detach(logits)
                if len(logits) == 1:
                    logits = logits[0]

                # SPAGHETTI CODE!!!
                get_labels_from_logits = GET_LABELS_FROM_LOGITS[model.guidance_config.guidance_module_type]
                if get_labels_from_logits is not None:
                    logits, labels = get_labels_from_logits(logits)
                    return loss, logits, labels

                return loss, logits, None

    def get_train_dataloader(self) -> DataLoader:
        """
        Basically this is the same as the default implementation from Huggingface, except that we retain unused columns.
        """
        if self.train_dataset is None:
            raise ValueError("Trainer: training requires a train_dataset.")

        train_dataset = self.train_dataset
        data_collator = self.data_collator
        # No need to remove unused columns.

        dataloader_params = {
            "batch_size": self._train_batch_size,
            "collate_fn": data_collator,
            "num_workers": self.args.dataloader_num_workers,
            "pin_memory": self.args.dataloader_pin_memory,
            "persistent_workers": self.args.dataloader_persistent_workers,
        }

        if not isinstance(train_dataset, torch.utils.data.IterableDataset):
            dataloader_params["sampler"] = self._get_train_sampler()
            dataloader_params["drop_last"] = self.args.dataloader_drop_last
            dataloader_params["worker_init_fn"] = seed_worker

        # accelerator should put dataloader on proper device
        return self.accelerator.prepare(DataLoader(train_dataset, **dataloader_params), device_placement=[True])

    def get_eval_dataloader(self, eval_dataset: Optional[Dataset] = None) -> DataLoader:
        """
        Basically this is the same as the default implementation from Huggingface, except that we retain unused columns.
        """
        if eval_dataset is None and self.eval_dataset is None:
            raise ValueError("Trainer: evaluation requires an eval_dataset.")
        eval_dataset = eval_dataset if eval_dataset is not None else self.eval_dataset
        data_collator = self.data_collator

        dataloader_params = {
            "batch_size": self.args.eval_batch_size,
            "collate_fn": data_collator,
            "num_workers": self.args.dataloader_num_workers,
            "pin_memory": self.args.dataloader_pin_memory,
            "persistent_workers": self.args.dataloader_persistent_workers,
        }

        if not isinstance(eval_dataset, torch.utils.data.IterableDataset):
            dataloader_params["sampler"] = self._get_eval_sampler(eval_dataset)
            dataloader_params["drop_last"] = self.args.dataloader_drop_last

        # accelerator should put dataloader on proper device
        return self.accelerator.prepare(DataLoader(eval_dataset, **dataloader_params), device_placement=[True])

    def create_optimizer(self):
        """
        Setup the optimizer. This optimizer should only optimize the parameters of guidance modules.

        We provide a reasonable default that works well. If you want to use something else, you can pass a tuple in the
        Trainer's init through `optimizers`, or subclass and override this method in a subclass.
        """
        assert hasattr(self.model, "guidance_modules"), ("The provided model doesn't have an attribute named "
                                                         "'guidance_modules' and thus cannot be trained using "
                                                         "GuidanceTrainer.")

        if self.optimizer is None:
            decay_parameters = self.get_decay_parameter_names(self.model.guidance_modules)
            optimizer_grouped_parameters = [
                {
                    "params": [
                        p for n, p in self.model.guidance_modules.named_parameters()
                        if (n in decay_parameters and p.requires_grad)
                    ],
                    "weight_decay": self.args.weight_decay,
                },
                {
                    "params": [
                        p for n, p in self.model.guidance_modules.named_parameters()
                        if (n not in decay_parameters and p.requires_grad)
                    ],
                    "weight_decay": 0.0,
                },
            ]

            optimizer_cls, optimizer_kwargs = Trainer.get_optimizer_cls_and_kwargs(self.args)

            self.optimizer = optimizer_cls(optimizer_grouped_parameters, **optimizer_kwargs)
            if optimizer_cls.__name__ == "Adam8bit":
                import bitsandbytes

                manager = bitsandbytes.optim.GlobalOptimManager.get_instance()

                skipped = 0
                for module in self.model.guidance_modules.modules():
                    if isinstance(module, nn.Embedding):
                        skipped += sum({p.data_ptr(): p.numel() for p in module.parameters()}.values())
                        logger.info(f"skipped {module}: {skipped / 2 ** 20}M params")
                        manager.register_module_override(module, "weight", {"optim_bits": 32})
                        logger.debug(f"bitsandbytes: will optimize {module} in fp32")
                logger.info(f"skipped: {skipped / 2 ** 20}M params")

        if is_sagemaker_mp_enabled():
            self.optimizer = smp.DistributedOptimizer(self.optimizer)
        return self.optimizer

